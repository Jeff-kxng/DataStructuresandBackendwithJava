package Arrays;

public class AssignValues {
    public static void main(String[] args) {
        int number[] = {1, 2, 3, 4, 5}; // array declared and initialized

        for (int i = 0; i < number.length; i++) {
            System.out.println("Element at index " + i + ": " + number[i]);
        }
    }
}
